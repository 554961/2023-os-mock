    <h1 class="alert alert-info">How to deal with Extreme Temperatures</h1>
    Dealing with excess temperatures involves staying cool, hydrated, and limiting activity, using methods like seeking air conditioning or shade, wearing light clothing, drinking cool water, taking cool showers, applying cold packs to pulse points, and avoiding exertion during the hottest hours. For severe heat illness, move the person to a cool place, loosen clothes, cool the skin with water and fanning, elevate their legs, and give small sips of water while seeking urgent medical help, as this could be heatstroke. 
    For Personal Cooling & Prevention: <hr>
    Seek cool environments: Use air conditioning, fans, or find shade; close curtains during the day. 
    Hydrate constantly: Drink plenty of cool water or sports drinks, even before feeling thirsty; avoid alcohol and caffeine. 
    Cool your body: Take cool showers or baths, use damp cloths on skin, or apply cold packs to neck, armpits, and wrists. 
    Dress smart: Wear loose, lightweight, light-colored clothing. 
    Limit activity: Avoid strenuous activities and exercise during the hottest parts of the day (usually 11 a.m. to 3 p.m.). 
    <hr>
    For Heat Exhaustion/Stroke (First Aid):
    Move to cool place: Get the person out of the heat immediately. 
    Lie down & elevate legs: Have them lie down and raise their legs slightly. 
    Cool the skin: Spray with cool water, sponge them, apply cool cloths, and fan them. 
    Hydrate (if conscious): Give small sips of cool water or sports drinks. 
    <hr>
    Call for help: Seek immediate medical attention if symptoms worsen or are severe, as this can be life-threatening heatstroke, according to the CDC. 
    At Work (Employer Actions):
    Provide cooling options like fans, AC, or cool water.
    Schedule work for cooler times and provide rest breaks in cool conditions.
    Reduce heat sources and use physical barriers. 
