    <h1 class="alert alert-info">How to deal with Hay Fever</h1> 
    To manage hay fever, focus on reducing pollen exposure by keeping windows closed, wearing wraparound sunglasses, and showering immediately after being outside. Over-the-counter remedies like non-drowsy antihistamine tablets, steroid nasal sprays, and eye drops are highly effective, especially if started two weeks before the season begins. 
    Preventive and Environmental Measures 
    <hr>
    Check Forecasts: Monitor pollen counts and avoid high-pollen areas (grassy, open spaces).
    Barrier Protection: Apply a small amount of petroleum jelly (Vaseline) inside the nostrils to trap pollen.
    At Home: Keep doors and windows closed, use a HEPA filter vacuum, and avoid drying clothes outside.
    Personal Hygiene: Shower, wash hair, and change clothes after returning from outdoors.
    Pets: Wipe pets down with a damp cloth if they have been outside to remove pollen from their fur. 
    <hr>
    Medication and Treatments
    Antihistamine Tablets: Treat sneezing and itchy eyes; non-drowsy options are best.
    Steroid Nasal Sprays: Reduce inflammation, sneezing, and congestion (most effective when used regularly).
    Eye Drops: Use anti-allergy drops for itchy, watery eyes.
    Nasal Rinses: Use saltwater solutions to flush out nasal passages. 
    <hr>
    When to Seek Medical Advice
    Consult a pharmacist or GP if symptoms are severe, affecting sleep/daily life, or if over-the-counter remedies are not working. People with asthma should ensure they have their reliever inhaler with them. 
