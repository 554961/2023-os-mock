    <strong>Managing asthma</strong> <br> Managing this effectively involves using a daily preventer inhaler, keeping a reliever inhaler (usually blue) with you at all times, and identifying/avoiding triggers like smoke, pollen, and pet dander. Work with a doctor to create a written asthma action plan, take vaccinations, exercise safely, and use a spacer with inhalers for better efficacy. 
    <hr>
    <strong>Key Management Strategies</strong> <br>
    Medication Adherence: Use steroid preventer inhalers daily to reduce inflammation, even when symptoms are absent. Use reliever inhalers (e.g., salbutamol) immediately when symptoms arise.
    Identify Triggers: Common triggers include tobacco smoke, air pollution, pollen, pets, mold, and cold air.
    Action Plan: Create a personalised asthma action plan with a doctor to know when to adjust medication and when to seek emergency care.
    Technique: Use a spacer device with your metered-dose inhaler to ensure medication reaches the lungs.
    Lifestyle: Maintain a healthy weight, get vaccinated (flu/pneumonia), and consider low-impact, regular exercise (e.g., walking). 
    <hr>
    <strong>Dealing with an Asthma Attack</strong> <br>
    Sit up straight: Try to remain calm.
    Use Reliever Inhaler: Take one puff every 30-60 seconds, up to 10 puffs.
    Call Emergency Services: Dial 999/112 if you feel worse, have no inhaler, or symptoms don't improve after 10 puffs. 
    When to Seek Medical Help
    If you are waking up at night due to symptoms.
    If you need to use your reliever inhaler three or more times a week.
    If your symptoms are not improving with treatment. 
